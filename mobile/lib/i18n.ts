const translations: Record<Language, Record<string, any>> = { en, sw };

export function t(lang: Language, namespace: string, key: string, params?: Record<string, string | number>): string {
  const ns = translations[lang]?.[namespace] ?? translations['en']?.[namespace] ?? {};
  let value = ns[key] ?? translations['en']?.[namespace]?.[key] ?? key;
  if (params && typeof value === 'string') {
    Object.entries(params).forEach(([k, v]) => {
      value = value.replace(`{{${k}}}`, String(v));
    });
  }
  return value;
}

export function useT(lang: Language, namespace: string) {
  return (key: string, params?: Record<string, string | number>) => t(lang, namespace, key, params);
}
